const string TITULO = "Operadores";
Console.WriteLine(TITULO);

int numero1 = 3456;
int numero2 = 98;
double resultado = 0;

//SUMA +
resultado = numero1 + numero2;
Console.WriteLine("Operacion Suma: " + resultado);

//RESTA -
resultado = numero1 - numero2;
Console.WriteLine("Operacion Resta: " + resultado);

//MULTIPLICACION *
resultado = numero1 * numero2;
Console.WriteLine("Operacion Multiplicacion: " + resultado);

//DIVISION /
resultado = numero1 / numero2;
Console.WriteLine("Operacion Division: " + resultado);

//MODULO %
resultado = numero1 % numero2;
Console.WriteLine("Operacion Modulo: " +  resultado);